#include "../push_swap.h"

void	ft_s(int *a);
{
	int tmp;

	tmp = a[0];
	a[1] = a[0];
	a[0] = tmp;
}

void	ft_ss(int *a, int *b)
{
	ft_s(a);
	ft_s(b);
}
